const fields = {
  symbol: document.querySelector("#ticket-symbol"),
  score: document.querySelector("#ticket-score"),
  signal: document.querySelector("#ticket-signal"),
  entry: document.querySelector("#ticket-entry"),
  stop: document.querySelector("#ticket-stop"),
  targets: document.querySelector("#ticket-targets"),
  position: document.querySelector("#ticket-position"),
  loss: document.querySelector("#ticket-loss"),
  market: document.querySelector("#ticket-market"),
};

let currentLanguage = "en";
let currentSymbol = "NVDA";

function setTextContent(language) {
  const dictionary = translations[language];
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (!dictionary[key]) return;
    node.innerHTML = dictionary[key];
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((node) => {
    const pairs = node.dataset.i18nAttr.split(",");
    pairs.forEach((pair) => {
      const [attr, key] = pair.split(":");
      if (attr && key && dictionary[key]) {
        node.setAttribute(attr, dictionary[key]);
      }
    });
  });
}

function setLanguage(language) {
  currentLanguage = language;
  setTextContent(language);
  updateTicket(currentSymbol);

  document.querySelectorAll(".lang-button").forEach((button) => {
    const active = button.dataset.lang === language;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", active ? "true" : "false");
  });
}

function updateTicket(symbol) {
  currentSymbol = symbol;
  const sample = samples[currentLanguage][symbol];
  if (!sample) return;

  fields.symbol.textContent = symbol;
  fields.score.textContent = sample.score;
  fields.signal.textContent = sample.signal;
  fields.entry.textContent = sample.entry;
  fields.stop.textContent = sample.stop;
  fields.targets.textContent = sample.targets;
  fields.position.textContent = sample.position;
  fields.loss.textContent = sample.loss;
  fields.market.textContent = sample.market;
}

document.querySelectorAll(".tab-button").forEach((button) => {
  button.addEventListener("click", () => {
    const symbol = button.dataset.symbol;

    document.querySelectorAll(".tab-button").forEach((item) => {
      const active = item === button;
      item.classList.toggle("is-active", active);
      item.setAttribute("aria-selected", active ? "true" : "false");
    });

    updateTicket(symbol);
  });
});

document.querySelectorAll(".lang-button").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

setLanguage("en");
